import Foundation
import UIKit

extension UIFont {

    static var primaryFontRegularLittle: UIFont {
        return UIFont(name: "QuicksandBook-Regular", size: 12) ?? UIFont.systemFont(ofSize: 12)
    }

    static var primaryFontRegularMedium: UIFont {
        return UIFont(name: "QuicksandBook-Regular", size: 15) ?? UIFont.systemFont(ofSize: 15)
    }

    static var primaryFontRegularMediumTow: UIFont {
        return UIFont(name: "QuicksandBook-Regular", size: 18) ?? UIFont.systemFont(ofSize: 18)
    }

    static var primaryFontRegularLarger: UIFont {
        return UIFont(name: "QuicksandBook-Regular", size: 22) ?? UIFont.systemFont(ofSize: 22)
    }

    static var primaryFontRegularLargerTow: UIFont {
        return UIFont(name: "QuicksandBook-Regular", size: 24) ?? UIFont.systemFont(ofSize: 24)
    }

    static var primaryFontRegularBig: UIFont {
        return UIFont(name: "QuicksandBook-Regular", size: 30) ?? UIFont.systemFont(ofSize: 30)
    }

    static var primaryFontBoldLittle: UIFont {
        return UIFont(name: "QuicksandBold-Regular", size: 12) ?? UIFont.systemFont(ofSize: 12)
    }

    static var primaryFontBoldMedium: UIFont {
        return UIFont(name: "QuicksandBold-Regular", size: 15) ?? UIFont.systemFont(ofSize: 15)
    }

    static var primaryFontBoldMediumTow: UIFont {
        return UIFont(name: "QuicksandBold-Regular", size: 18) ?? UIFont.systemFont(ofSize: 18)
    }

    static var primaryFontBoldLarger: UIFont {
        return UIFont(name: "QuicksandBold-Regular", size: 22) ?? UIFont.systemFont(ofSize: 22)
    }

    static var primaryFontBoldLargerTow: UIFont {
        return UIFont(name: "QuicksandBold-Regular", size: 24) ?? UIFont.systemFont(ofSize: 24)
    }

    static var primaryFontBoldBig: UIFont {
        return UIFont(name: "QuicksandBold-Regular", size: 30) ?? UIFont.systemFont(ofSize: 30)
    }
}


